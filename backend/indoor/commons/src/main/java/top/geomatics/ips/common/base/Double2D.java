public class Double2D {
	private Double x;
	private Double y;
	
	
	public Double2D() {
		super();
	}


	public Double2D(Double x, Double y, Double z) {
		super();
		this.x = x;
		this.y = y;
	}


	public Double getX() {
		return x;
	}


	public void setX(Double x) {
		this.x = x;
	}


	public Double getY() {
		return y;
	}


	public void setY(Double y) {
		this.y = y;
	}


}
