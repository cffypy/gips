package top.geomatics.ips.common.base;

/**
 * @author whudyj
 *
 */
public class Double3D {
	private Double x;
	private Double y;
	private Double z;
	
	
	public Double3D() {
		super();
	}


	public Double3D(Double x, Double y, Double z) {
		super();
		this.x = x;
		this.y = y;
		this.z = z;
	}


	public Double getX() {
		return x;
	}


	public void setX(Double x) {
		this.x = x;
	}


	public Double getY() {
		return y;
	}


	public void setY(Double y) {
		this.y = y;
	}


	public Double getZ() {
		return z;
	}


	public void setZ(Double z) {
		this.z = z;
	}
	
	

}
