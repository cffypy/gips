package top.geomatics.ips.service;


/**
 * @author chenfa
 * 定位结果类
 */
public class PositionResult {
    public double x;
    public double y;

    public PositionResult() {
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
